<h1>About</h1>
<blockquote>The problem about Latin is that you can study it for six years and still not be able to read a Latin sentence.</blockquote>
<p>Such begins Professor William C. Dowling’s webpage on his unorthodox technique for
learning to read Latin fluently: <a href="http://www.rci.rutgers.edu/~wcd/Latin.htm">Latin by the Dowling Method</a>.
His recipe for success is as follows:</p>
<ol>
<li>Learn a few simple concepts about Latin grammar, documented on his webpage (1 hour).</li>
<li>Memorize six of the tables at the back of <cite>Wheelock’s Latin</cite> (6 months).</li>
<li>Work through <cite>Lingua Latina</cite>, a wonderful workbook on Latin <em>written in Latin</em> (18 months).</li>
</ol>
<p>This website will help you with the second goal: memorizing the tables. On his webpage, Professor
Dowling recommends that you write out each table 200 times (but only 100 times for the Adjectives table).
Using this website, you can type the tables instead of writing them out by hand. Moreover, we mark your words
in green or red to show which are correct and which are incorrect.</p>
